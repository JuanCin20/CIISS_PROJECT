	----CREACIÓN DE LAS TABLAS
	CREATE TABLE Tabla_Tipo_Usuario (
		ID_Tipo_Usuario INT IDENTITY (1, 1) PRIMARY KEY,
		Nombre_Tipo_Usuario VARCHAR (15) CHECK (
			Nombre_Tipo_Usuario IN ('Administrador', 'Empleado')
		) NOT NULL
	);

GO
	CREATE TABLE Tabla_Usuario (
		ID_Usuario INT IDENTITY (1, 1) PRIMARY KEY,
		ID_Tipo_Usuario INT FOREIGN KEY REFERENCES Tabla_Tipo_Usuario (ID_Tipo_Usuario) NOT NULL,
		Nombre_Usuario VARCHAR (50) NOT NULL,
		Apellido_Usuario VARCHAR (50) NOT NULL,
		E_Mail_Usuario VARCHAR (30) NOT NULL,
		Password_Usuario VARCHAR (150) NOT NULL,
		Reestablecer_Password_Usuario BIT DEFAULT 1 NOT NULL,
		Estado_Usuario BIT DEFAULT 1 NOT NULL,
		Fecha_Registro_Usuario DATETIME DEFAULT GETDATE() NOT NULL,
		Ruta_Imagen_Usuario VARCHAR (255) NULL,
		Nombre_Imagen_Usuario VARCHAR (255) DEFAULT 'Image_Error.png' NULL
	);

GO
	CREATE TABLE Tabla_Categoria_Insumo (
		ID_Categoria_Insumo INT IDENTITY (1, 1) PRIMARY KEY,
		Nombre_Categoria_Insumo VARCHAR (50) NOT NULL,
		Descripcion_Categoria_Insumo TEXT NOT NULL,
		Estado_Categoria_Insumo BIT DEFAULT 0 NOT NULL,
		Fecha_Registro_Categoria_Insumo DATETIME DEFAULT GETDATE() NOT NULL
	);

GO
	CREATE TABLE Tabla_Proveedor_Insumo (
		ID_Proveedor_Insumo INT IDENTITY (1, 1) PRIMARY KEY,
		Nombre_Proveedor_Insumo VARCHAR (50) NOT NULL,
		Telefono_Proveedor_Insumo INT NOT NULL,
		E_Mail_Proveedor_Insumo VARCHAR (30),
		Direccion_Proveedor_Insumo VARCHAR (50) NOT NULL,
		Estado_Proveedor_Insumo BIT DEFAULT 0 NOT NULL,
		Fecha_Registro_Proveedor_Insumo DATETIME DEFAULT GETDATE() NOT NULL
	);

GO
	CREATE TABLE Tabla_Insumo (
		ID_Insumo INT IDENTITY (1, 1) PRIMARY KEY,
		ID_Categoria_Insumo INT FOREIGN KEY REFERENCES Tabla_Categoria_Insumo (ID_Categoria_Insumo) NOT NULL,
		ID_Proveedor_Insumo INT FOREIGN KEY REFERENCES Tabla_Proveedor_Insumo (ID_Proveedor_Insumo) NOT NULL,
		Nombre_Insumo VARCHAR (110) NOT NULL,
		Descripcion_Insumo TEXT NOT NULL,
		Unidad_Medida_Insumo VARCHAR (20) NOT NULL,
		Precio_Insumo DECIMAL (10, 2) NOT NULL,
		Stock_Insumo INT NOT NULL,
		Estado_Insumo BIT DEFAULT 1 NOT NULL,
		Fecha_Registro_Insumo DATETIME DEFAULT GETDATE() NOT NULL,
		Fecha_Vencimiento_Insumo DATE NULL,
		Ruta_Imagen_Insumo VARCHAR (255) NULL,
		Nombre_Imagen_Insumo VARCHAR (255) DEFAULT 'Image_Error.png' NULL
	);

GO
	CREATE TABLE Tabla_Middle (
		ID_Middle INT IDENTITY (1, 1) PRIMARY KEY,
		ID_Usuario INT FOREIGN KEY REFERENCES Tabla_Usuario (ID_Usuario) NOT NULL,
		ID_Insumo INT FOREIGN KEY REFERENCES Tabla_Insumo (ID_Insumo) NOT NULL,
		Cantidad_Insumo_Middle INT NOT NULL
	);

GO
	CREATE TABLE Tabla_Departamento (
		ID_Departamento INT IDENTITY (1, 1) PRIMARY KEY,
		Nombre_Departamento VARCHAR (80) NOT NULL
	);

GO
	CREATE TABLE Tabla_Provincia (
		ID_Provincia INT IDENTITY (1, 1) PRIMARY KEY,
		Nombre_Provincia VARCHAR (80) NOT NULL,
		ID_Departamento INT FOREIGN KEY REFERENCES Tabla_Departamento (ID_Departamento) NOT NULL
	);

GO
	CREATE TABLE Tabla_Distrito (
		ID_Distrito INT IDENTITY (1, 1) PRIMARY KEY,
		Nombre_Distrito VARCHAR (80) NOT NULL,
		ID_Provincia INT FOREIGN KEY REFERENCES Tabla_Provincia (ID_Provincia) NOT NULL,
		ID_Departamento INT FOREIGN KEY REFERENCES Tabla_Departamento (ID_Departamento) NOT NULL
	);

GO
	CREATE TABLE Tabla_Movimiento_Inventario (
		ID_Movimiento_Inventario INT IDENTITY (1, 1) PRIMARY KEY,
		ID_Usuario INT FOREIGN KEY REFERENCES Tabla_Usuario (ID_Usuario) NOT NULL,
		Tipo_Movimiento_Inventario VARCHAR (10) CHECK (
			Tipo_Movimiento_Inventario IN ('Entrada', 'Salida')
		) NOT NULL,
		Cantidad_Insumo_Movimiento_Inventario INT NOT NULL,
		Monto_Total_Movimiento_Inventario DECIMAL (10, 2) NOT NULL,
		Restaurante_Movimiento_Inventario VARCHAR (50) NULL,
		Telefono_Movimiento_Inventario INT NULL,
		Direccion_Movimiento_Inventario VARCHAR (150) NULL,
		ID_Distrito INT FOREIGN KEY REFERENCES Tabla_Distrito (ID_Distrito) NULL,
		Fecha_Movimiento_Inventario DATETIME DEFAULT GETDATE() NOT NULL
	);

GO
	CREATE TABLE Tabla_Detalle_Movimiento_Inventario (
		ID_Detalle_Movimiento_Inventario INT IDENTITY (1, 1) PRIMARY KEY,
		ID_Movimiento_Inventario INT FOREIGN KEY REFERENCES Tabla_Movimiento_Inventario (ID_Movimiento_Inventario) NOT NULL,
		ID_Insumo INT FOREIGN KEY REFERENCES Tabla_Insumo (ID_Insumo) NOT NULL,
		Cantidad_Insumo_Detalle_Movimiento_Inventario INT NOT NULL,
		Monto_Total_Detalle_Movimiento_Inventario DECIMAL (10, 2) NOT NULL
	);

